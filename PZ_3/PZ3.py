# Проверить истинность высказывания Среди трех данных целых чисел есть хотя бы одна пара взаимно противоположных
try:
    # Ввод трех целых чисел
    a = int(input("Введите первое целое число: "))
    b = int(input("Введите второе целое число: "))
    c = int(input("Введите третье целое число: "))

    # Проверка на наличие пары взаимно противоположных
    if (a == -a) or (b == -b) or (c == -c):
        print("Среди введенных чисел есть хотя бы одна пара взаимно противоположных.")
    else:
        print("Введенные числа не содержат пары взаимно противоположных.")

except ValueError:
    print("Ошибка: Пожалуйста, введите целые числа.")
