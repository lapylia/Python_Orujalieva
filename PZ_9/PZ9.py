#посчитать количество уникальных слов в заданном
#предложении «Изучаем язык Питон». Вывести на экран каждую пару
#«ключ:значение».
sentence = "Изучаем язык Питон"
# Разбиваем предложение на слова
words = sentence.split()
# Создаем пустой словарь для подсчета уникальных слов
word_count = {}
# Подсчитываем количество употреблений каждого слова
for word in words:
    if word in word_count:
        word_count[word] += 1
    else:
        word_count[word] = 1
# Выводим каждую пару "ключ:значение"
for word, count in word_count.items():
    print(f"{word}: {count}")